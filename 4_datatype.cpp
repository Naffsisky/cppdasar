#include <iostream>
using namespace std;
int main()

{
int myNum = 1000;
cout << "Int " << myNum << endl;

float myNum1 = 5.75;
cout << "Float " << myNum1 << endl;

double myNum2 = 19.99;
cout << "Double " << myNum2 << endl;


bool isCodingFun = true;
bool isFishTasty = false;
cout << "true " << isCodingFun << endl;  // Outputs 1 (true)
cout << "false " << isFishTasty << endl;  // Outputs 0 (false)

char myGrade = 'B';
cout << "Char" << myGrade << endl;

char a = 65, b = 66, c = 67;
cout << a << endl;
cout << b << endl;
cout << c << endl;

// Fungsi e untuk membuat 0
// Misal e10, berarti 0nya ada 10. 0000000000
float f1 = 35e3;
double d1 = 12E4;
cout << f1 << endl;
cout << d1 << endl;

string closing = "Bye";
cout << closing;

return 0;
}

